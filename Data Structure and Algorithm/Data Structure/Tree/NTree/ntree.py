class NAryTreeNode():
    def __init__(self, e):  # (e,children=[])不对
        self.id = e['logid']
        self.data = e  # dict
        self.children = []  # list

    # 添加一个孩子节点
    def add_children(self, node):
        self.children.append(node)


class Process():
    def __init__(self, event):
        self.event = event

    def preorder_tree(self, root):
        if not root:
            return []
        res = []
        res.append(root)
        for child in root.children:
            res.extend(self.preorder_tree(child))
        return res

    def build_tree(self):
        root_nodes_list = []
        for e in self.event:
            if not e:
                return
            if e['parentprocessguid'] is None:
                root = NAryTreeNode(e)
                root_nodes_list.append(root)
                continue
            for root in root_nodes_list:
                all_nodes = self.preorder_tree(root)
                for node_index in range(len(all_nodes)):
                    if e['parentprocessguid'] == all_nodes[node_index].data['processguid']:
                        child_node = NAryTreeNode(e)
                        all_nodes[node_index].add_children(child_node)
        return root_nodes_list

    def preorder_show_tree(self, root, depth):
        if not root:
            return
        depth += 1
        for child_index in range(len(root.children)):
            if child_index == len(root.children) - 1:
                print('|   ' * (depth - 1) + '└──' + str(root.children[child_index].id))
            else:
                print('|   ' * (depth - 1) + '├──' + str(root.children[child_index].id))
            self.preorder_show_tree(root.children[child_index], depth)

    def print_tree(self):
        root_nodes_list = self.build_tree()
        print(root_nodes_list)
        for tree in self.build_tree():
            print('\n' + 'Show tree, current tree.id is ' + str(tree.id))
            print(tree.id)
            self.preorder_show_tree(tree, 0)

